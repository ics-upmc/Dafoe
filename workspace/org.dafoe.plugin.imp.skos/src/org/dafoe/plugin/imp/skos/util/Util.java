package org.dafoe.plugin.imp.skos.util;

import java.util.ArrayList;
import java.util.List;

public class Util {


   
   public static <T> List<T> toList(T [] tcsArray){
		List<T> tcsList= new ArrayList<T>();
		
		for(T tc: tcsArray){
			tcsList.add(tc);
		}
		
		return tcsList;
	}
  
   
}
